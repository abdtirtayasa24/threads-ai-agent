@router.post("/{draft_id}/regenerate")
def regenerate_draft(draft_id: uuid.UUID, db: Session = Depends(get_db)):
    """Mark a draft to be regenerated by the AI."""
    draft = db.query(ThreadPostDraft).filter(ThreadPostDraft.id == draft_id).first()
    if not draft:
        raise HTTPException(status_code=404, detail="Draft not found")
        
    draft.status = "pending_regeneration"
    db.add(ThreadPostLog(draft_id=draft.id, action="regenerate", detail="Regeneration requested via API"))
    db.commit()
    return {"status": "ok", "message": f"Draft {draft_id} marked for regeneration."}